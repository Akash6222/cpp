# include <iostream>
using namespace std;

int main() {

    int number[10] = {0};    // Initializing the array with some values

    // Printing all elements of the array
    for (int i = 0; i < 10; i++) {
        cout << number[i] << " ";
    }

    cout << endl; // Print a newline after printing the array elements

    return 0;

}