// Left shift means shifting the bit to left 
// 5<<1 this means shifting all the bit to 1 left of it
// 0000000....00101 after left shift 0000000....01010

#include<iostream>
using namespace std;

int main() {
    cout<<"Left Shift of (17<<1) is "<< (17<<1)<<endl;
    cout<<"Left Shift of (17<<2) is "<< (17<<2)<<endl;
    cout<<"Left Shift of (19<<1) is "<< (19<<1)<<endl;
    cout<<"Left Shift of (19<<2) is "<< (19<<2)<<endl;
}