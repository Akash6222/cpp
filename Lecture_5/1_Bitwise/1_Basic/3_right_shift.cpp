// Right shift means shifting the bit to right
// 5>>1 this means shifting all the bit to 1 right of it
// 0000000....00101 after right shift 0000000....00010

#include<iostream>
using namespace std;

int main() {
    cout<<"Right Shift of (17>>1) is "<< (17>>1)<<endl;
    cout<<"Right Shift of (17>>2) is "<< (17>>2)<<endl;
    cout<<"Right Shift of (19>>1) is "<< (19>>1)<<endl;
    cout<<"Right Shift of (19>>2) is "<< (19>>2)<<endl;
}